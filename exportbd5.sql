  CREATE OR REPLACE PROCEDURE "ADMINISTRADOR"."CALCULAR" (            x in out number,                              
                                                 numero   IN NUmber,
                                                 digito IN NUmber)
IS
CADENA varchar(1000);
     var1 number;
     var2 number;  
Begin
    FOR i IN 0..numero LOOP
     CADENA:=CADENA||To_char(i*i)||',';
    END LOOP;
    Var1:=length(CADENA);
    Var2:=length(replace(CADENA,To_char(digito),''));
    x:=var1-var2;
    DBMS_OUTPUT.put_line(To_char(CADENA));
END;

